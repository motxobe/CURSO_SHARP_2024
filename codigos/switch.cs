switch (valor) // si por ejemplo valor es 4
{
    case 1:
        Console.WriteLine("UNO");
        break;
    case 2:
        Console.WriteLine("DOS");
        break;
    case 3:
        Console.WriteLine("TRES");
        break;
    case 4:
        Console.WriteLine("CUATRO");
        break;
    case 5:
        Console.WriteLine("CINCO");
        break;
    case 6:
        Console.WriteLine("SEIS");
        break;
    case 7:
        Console.WriteLine("SIETE");
        break;
    default:
        Console.WriteLine(" VALOR NO ESTÁ ENTRE 1 Y 7");
        break;
}
// Outputs "CUATRO" (VALOR 4)